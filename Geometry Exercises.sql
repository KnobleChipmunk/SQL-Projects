/* GEOMETRY EXERCISES */

--SQUARE
DECLARE @SQ GEOMETRY =
GEOMETRY::STGeomFromText('LINESTRING (10 10, 10 100, 100 100, 100 10, 10 10)', 0);

SELECT @SQ

--CIRCLE
DECLARE @CIR GEOMETRY = 
GEOMETRY::Parse('CIRCULARSTRING(3 2, 2 3, 1 2, 2 1, 3 2)');

SELECT @CIR

--TRIANGLE
DECLARE @TRI GEOMETRY = 
GEOMETRY::STGeomFromText('POLYGON((100 100, 200 300, 300 100, 100 100))', 0);

SELECT @TRI

--FILLED SQUARE
DECLARE @SQFILLED GEOMETRY = 
GEOMETRY::STGeomFromText('POLYGON((10 10, 10 100, 100 100, 100 10, 10 10))', 0);

SELECT @SQFILLED

--TRIANGLE/SQUARE IN LAYERS
DECLARE @SQUARE GEOMETRY =
GEOMETRY::STGeomFromText('POLYGON((15 15, 15 250, 250 250, 250 15, 15 15))', 0),
@TRIANGLE GEOMETRY = 
GEOMETRY::STGeomFromText('POLYGON((100 100, 200 300, 300 100, 100 100))', 0);

SELECT @SQUARE
UNION ALL
SELECT @TRIANGLE

--LAYER UNION
DECLARE @SQLAYER GEOMETRY =
GEOMETRY::STGeomFromText('POLYGON((15 15, 15 250, 250 250, 250 15, 15 15))', 0),
@TRILAYER GEOMETRY = 
GEOMETRY::STGeomFromText('POLYGON((100 100, 200 300, 300 100, 100 100))', 0);

SELECT @SQLAYER.STUnion(@TRILAYER)

--LAYER INTERSECTION
DECLARE @SQLAYERINT GEOMETRY =
GEOMETRY::STGeomFromText('POLYGON((15 15, 15 250, 250 250, 250 15, 15 15))', 0),
@TRILAYERINT GEOMETRY = 
GEOMETRY::STGeomFromText('POLYGON((100 100, 200 300, 300 100, 100 100))', 0);

SELECT @SQLAYERINT.STIntersection(@TRILAYERINT)

--OVERLAP
DECLARE @SQLAYERLAP GEOMETRY =
GEOMETRY::STGeomFromText('POLYGON((15 15, 15 250, 250 250, 250 15, 15 15))', 0),
@TRILAYERLAP GEOMETRY = 
GEOMETRY::STGeomFromText('POLYGON((100 100, 200 300, 300 100, 100 100))', 0);

SELECT @SQLAYERLAP.STSymDifference(@TRILAYERLAP)

--CENTER
DECLARE @SQCENT GEOMETRY 
= GEOMETRY::STGeomFromText('POLYGON((100 100, 100 50, 50 50, 50 100, 100 100))', 0);
 
SELECT @SQCENT.STCentroid().STBuffer(100)
UNION ALL
SELECT @SQCENT